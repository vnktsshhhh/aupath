async function main() {
  console.log("🌱 Starting seed...");

  // Upsert companies
  const companyMap = new Map<string, string>();
  for (const c of companies) {
    const compSlug = slug(c.name);
    const company = await prisma.company.upsert({
      where: { slug: compSlug },
      update: {
        description: c.description,
        industry: c.industry,
        size: c.size,
        location: c.location,
        state: c.state,
        website: c.website,
        logoUrl: c.logoUrl,
      },
      create: {
        name: c.name,
        slug: compSlug,
        description: c.description,
        industry: c.industry,
        size: c.size,
        location: c.location,
        state: c.state,
        website: c.website,
        logoUrl: c.logoUrl,
      },
    });
    companyMap.set(c.name, company.id);
    console.log(`  ✓ Company: ${c.name}`);
  }

  // Create jobs
  let created = 0;
  for (const j of jobs) {
    const companyId = companyMap.get(j.companyName);
    if (!companyId) {
      console.warn(`  ⚠ Company not found: ${j.companyName}`);
      continue;
    }

    const jobSlug = slug(`${j.title}-${j.companyName}-${j.location}`);
    const existing = await prisma.job.findFirst({ where: { slug: jobSlug } });
    if (existing) {
      console.log(`  → Skip (exists): ${j.title}`);
      continue;
    }

    await prisma.job.create({
      data: {
        title: j.title,
        companyId,
        description: j.description,
        requirements: j.requirements,
        responsibilities: j.responsibilities,
        skills: j.skills,
        location: j.location,
        state: j.state,
        workType: j.workType,
        employmentType: j.employmentType,
        salaryMin: j.salaryMin,
        salaryMax: j.salaryMax,
        visaSponsorship: j.visaSponsorship,
        workRights: j.workRights,
        industry: j.industry,
        experienceLevel: j.experienceLevel,
        isFeatured: j.isFeatured,
        slug: jobSlug,
        postedAt: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000),
      },
    });
    created++;
  }

  console.log(`\n✅ Seed complete: ${companies.length} companies, ${created} jobs created`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
